MicroOLED oled(PIN_RESET, DC_JUMPER);

Screen::Screen() {}

void Screen::setup()
{
  Serial.print("Setting up OLED...");

  oled.begin();
  oled.clear(ALL);
  oled.display();

  Serial.println("SUCCESS");
};

void Screen::println(String &line)
{
  oled.clear(PAGE);
  oled.setFontType(0);
  oled.setCursor(0, 3);
  oled.println(line);
  oled.display();
};

void Screen::drawImage()
{
  oled.clear(PAGE);
  oled.drawBitmap(issBitmap);
  oled.display();
};
